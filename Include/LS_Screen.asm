.label COLOR_RAM        = $D800


//-----------------------SUBS-----------------------------------
/*****************************************************************/

//-----------------------DATA-------------------------------
//LS_Screen: 		* = LS_Screen "LS_Screen"


//-----------------------MACROS-----------------------------
/*****************************************************************/
.macro FillScreen(addr, char){
/*
args: screen addres, character code
*/
		lda	#char
		ldy #0
fill:
		sta addr,y
		sta addr + 256,y
		sta addr + 512,y
		sta addr + 768,y
		iny
		bne fill

}

/*****************************************************************/

.macro FillColor(color){
/**
args: color: color code
*/	

		lda	#color
		ldy #0
fill:
		sta COLOR_RAM,y
		sta COLOR_RAM + 256,y
		sta COLOR_RAM + 512,y
		sta COLOR_RAM + 768,y
		iny
		bne fill
}


